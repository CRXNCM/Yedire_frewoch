      // Project category filtering
      document.addEventListener('DOMContentLoaded', function() {
        const projectLinks = document.querySelectorAll('.projects-nav .project-link');
        const projectCards = document.querySelectorAll('.project-card');
        
        projectLinks.forEach(link => {
          link.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Remove active class from all links
            projectLinks.forEach(l => l.classList.remove('active'));
            
            // Add active class to clicked link
            this.classList.add('active');
            
            // Remove highlighted class from all cards
            projectCards.forEach(card => card.classList.remove('highlighted'));
            
            // Highlight corresponding card
            const category = this.getAttribute('data-category');
            document.getElementById(category).classList.add('highlighted');
            
            // Scroll to project
            document.getElementById(category).scrollIntoView({
              behavior: 'smooth',
              block: 'nearest'
            });
          });
        });
      });
    </script>
